    private void tablitaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablitaMouseClicked
        int fila = tablita.getSelectedRow();
        btnIngresar.setText("ACTUALIZAR");
        btnCancelar.setVisible(true);
        if (fila != -1) {
            int id = (int) tablita.getValueAt(fila, 0);
            String tarea = (String) tablita.getValueAt(fila, 1);
            String desc = (String) tablita.getValueAt(fila, 2);

            txtid.setText(String.valueOf(id));
            txttarea.setText(tarea);
            txtdesc.setText(desc);
        }
    }//GEN-LAST:event_tablitaMouseClicked

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        int fila = tablita.getSelectedRow();
        if (fila != -1) {
            int respuesta = JOptionPane.showConfirmDialog(null, "¿SEGURO QUE QUIERES BORRARLO?");
            System.out.println(respuesta);
            if (respuesta == 0) {
                int id = (int) tablita.getValueAt(fila, 0);
                conexion c = new conexion();
                String sql = "DELETE FROM tarea WHERE id=" + id + "";
                c.exec(sql);
                cargarTabla();
                JOptionPane.showMessageDialog(null, "LO BORRAMOS DE PANA");
            }
        }
    }//GEN-LAST:event_EliminarActionPerformed

