    private void cargarTabla() {
        try {
            Object[] cabeceras = {"ID", "TAREA", "DESCRIPCION"};
            Object[][] cuerpo = {};

            DefaultTableModel modelo = new DefaultTableModel(cuerpo, cabeceras);
            String sql = "select * from tarea";

            conexion conn = new conexion();
            ResultSet resul = conn.get(sql);

            while (resul.next()) {
                Object[] elemento = {resul.getInt("id"), resul.getString("tarea"), resul.getString("descripcion")};
                modelo.addRow(elemento);
            }
            tablita.setModel(modelo);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String id = txtid.getText();
        String nombre = txttarea.getText();
        String desc = txtdesc.getText();

        System.out.println(btnIngresar.getText());
        if (btnIngresar.getText().equals("INGRESAR")) {
            conexion c = new conexion();
            String sql = "INSERT INTO tarea (id,tarea,descripcion) VALUES (null,'" + nombre + "','" + desc + "')";
            c.exec(sql);
            JOptionPane.showMessageDialog(null, "TAREA INGRESADA EXITOSAMENTE");
            cargarTabla();
        } else if (btnIngresar.getText().equals("ACTUALIZAR")) {
            conexion c = new conexion();
            String sql = "UPDATE tarea SET tarea='" + nombre + "', descripcion='" + desc + "' WHERE id=" + id + "";
            c.exec(sql);
            JOptionPane.showMessageDialog(null, "TAREA ACTUALIZADA EXITOSAMENTE");
            cargarTabla();
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

